-- ─── Table 1: global_players (Higher/Lower game) ────────────────────
CREATE TABLE IF NOT EXISTS global_players (
  id TEXT PRIMARY KEY,                    -- e.g. "messi"
  name TEXT NOT NULL,
  image TEXT NOT NULL,                    -- relative path e.g. "img/players/messi.jpg"
  career_goals INTEGER NOT NULL DEFAULT 0,
  career_trophies INTEGER NOT NULL DEFAULT 0,
  market_value NUMERIC NOT NULL DEFAULT 0,        -- USD millions
  transfer_from TEXT,
  transfer_to TEXT,
  transfer_fee NUMERIC NOT NULL DEFAULT 0,        -- USD millions
  transfermarkt_id INTEGER,              -- for automated scraping
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ─── Table 2: guess_players (Guess Who game) ────────────────────────
CREATE TABLE IF NOT EXISTS guess_players (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  image TEXT NOT NULL,
  club TEXT NOT NULL,
  nation TEXT NOT NULL,
  transfermarkt_id INTEGER,              -- for automated scraping
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ─── Table 3: grid_players (Grid Challenge game) ────────────────────
CREATE TABLE IF NOT EXISTS grid_players (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  clubs TEXT[] NOT NULL DEFAULT '{}',     -- PostgreSQL text array
  nation TEXT NOT NULL,
  stats TEXT[] NOT NULL DEFAULT '{}',     -- PostgreSQL array of stat IDs
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- App tables used by auth, dashboard, and leaderboard
CREATE TABLE IF NOT EXISTS profiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  username TEXT,
  avatar_url TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT profiles_username_length
    CHECK (username IS NULL OR char_length(trim(username)) BETWEEN 1 AND 32)
);

CREATE TABLE IF NOT EXISTS scores (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  mode TEXT NOT NULL,
  score INTEGER NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT scores_mode_check
    CHECK (mode IN ('scoreline', 'unscramble', 'whosaidit', 'higherlower', 'grid', 'transfer', 'guess')),
  CONSTRAINT scores_score_check
    CHECK (score >= 0 AND score <= 1000)
);

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS set_profiles_updated_at ON profiles;
CREATE TRIGGER set_profiles_updated_at
BEFORE UPDATE ON profiles
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

