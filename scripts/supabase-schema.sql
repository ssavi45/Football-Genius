CREATE OR REPLACE FUNCTION get_leaderboard(
  p_mode TEXT DEFAULT NULL,
  p_period TEXT DEFAULT 'all',
  p_limit INTEGER DEFAULT 50
)
RETURNS TABLE (
  rank BIGINT,
  user_id UUID,
  username TEXT,
  avatar_url TEXT,
  best_score INTEGER,
  games_played BIGINT
)
LANGUAGE sql
STABLE
AS $$
  WITH filtered_scores AS (
    SELECT s.user_id, s.score, s.created_at
    FROM scores s
    WHERE (p_mode IS NULL OR s.mode = p_mode)
      AND (
        p_period = 'all'
        OR (p_period = 'daily' AND s.created_at >= now() - interval '1 day')
        OR (p_period = 'weekly' AND s.created_at >= now() - interval '7 days')
      )
  ),
  aggregated AS (
    SELECT
      fs.user_id,
      MAX(fs.score)::INTEGER AS best_score,
      COUNT(*)::BIGINT AS games_played
    FROM filtered_scores fs
    GROUP BY fs.user_id
  ),
  ranked AS (
    SELECT
      ROW_NUMBER() OVER (ORDER BY a.best_score DESC, a.games_played DESC, a.user_id) AS rank,
      a.user_id,
      p.username,
      p.avatar_url,
      a.best_score,
      a.games_played
    FROM aggregated a
    LEFT JOIN profiles p ON p.id = a.user_id
  )
  SELECT
    r.rank,
    r.user_id,
    r.username,
    r.avatar_url,
    r.best_score,
    r.games_played
  FROM ranked r
  ORDER BY r.rank
  LIMIT GREATEST(COALESCE(p_limit, 50), 1);
$$;
